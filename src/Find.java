    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        int rVal = jFileChooser1.showOpenDialog(this);
        if (rVal == javax.swing.JFileChooser.APPROVE_OPTION) {
            fileI = jFileChooser1.getSelectedFile();
            jTextField2.setText(fileI.toString());
        }
        file = fileI.getAbsolutePath();
        if (jRadioButton3.isSelected() == true) {
            DatabaseConstructor.initialized = false;
            DatabaseConstructor.initialize(file);
        }
        if (jRadioButton4.isSelected() == true) {
            DatabaseConstructor.initialized = false;
            try {
                DocumentTermMatrixGen.initialize(file);
            } catch (Exception ex) {
                Logger.getLogger(Find.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_jButton2ActionPerformed

