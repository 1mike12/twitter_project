    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

        Calendar startT = Calendar.getInstance();
        String query = jTextField1.getText();
        if (jRadioButton3.isSelected() == true) {
            {
//                DatabaseConstructor.initialize(file);
                s = DatabaseConstructor.readResults(DatabaseConstructor.matchQuery(query));
                if (s.isEmpty()) {
                    s.add("No results found.");
                }
            }
        }
        if (jRadioButton4.isSelected() == true) {
            {
                try {
//                    DocumentTermMatrixGen.initialize(file);
                } catch (Exception ex) {
                    Logger.getLogger(Find.class.getName()).log(Level.SEVERE, null, ex);
                }
                s = DocumentTermMatrixGen.searchQuery(query);
            }
        }
        String TextR = "";
        //for (String s2 : s)
        for (int i = 0; i < s.size(); i++) {
            String s2 = s.get(i);
            TextR += i + ". " + s2 + "\n";
        }
        //jTextArea1.setText(s.toString());
        jTextArea1.setText(TextR);
        now = Calendar.getInstance();
        double timeTaken = now.getTimeInMillis() - startT.getTimeInMillis();
        jLabel4.setText("" + timeTaken + " milliseconds");
    }//GEN-LAST:event_jButton1ActionPerformed

